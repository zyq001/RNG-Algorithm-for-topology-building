package edu.bupt.zyq.wsn.router.mgr.calNeighbor;

public class Node {
	String netmask;
	String addr;
	String name;
	int neighborCount;
	
	public Node(String addr, String netmask, String name, int neighborCount) {
		this.neighborCount = neighborCount;
		this.netmask = netmask;
		this.addr = addr;
		this.name = name;
	}
}